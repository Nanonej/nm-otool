#include "libft.h"

int		ft_digitcount(int n)
{
	int ret;

	ret = 1;
	if (n == -2147483648)
	{
		ret += 2;
		n = 147483648;
	}
	if (n < 0)
	{
		n = -n;
		ret++;
	}
	if (n > 10)
		ret += ft_digitcount(n / 10);
	return (ret);
}
